hourly_rate = float(input("Введите ставку за час (в рублях): "))
hours_worked = float(input("Введите количество отработанных часов: "))

salary = hourly_rate * hours_worked

print("Зарплата:", salary, "рублей")

# float(input()) - получение числа с плавающей точкой от пользователя

# hourly_rate * hours_worked - формула для расчета зарплаты

# print - вывод результата, аргументы разделены запятыми