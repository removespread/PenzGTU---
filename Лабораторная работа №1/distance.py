x1 = float(input("Введите x-координату первой точки: "))
y1 = float(input("Введите y-координату первой точки: "))
x2 = float(input("Введите x-координату второй точки: "))
y2 = float(input("Введите y-координату второй точки: "))

dx = x2 - x1
dy = y2 - y1

distance = (dx**2 + dy**2)**0.5

print("Расстояние между точками:", distance)

# input() - получает ввод пользователя как строку
# float() - преобразует строку в число с плавающей точкой
# итого: float(input())

# dx и dy - разница между соответствующими координатами

# используется теорема Пифагора для вычисления расстояния
# (dx**2 + dy**2)**0.5 - корень из суммы квадратов

# print - вывод результата, с передачей аргументов через запятую