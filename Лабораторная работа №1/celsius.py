celsius = float(input("Введите температуру в градусах Цельсия: "))

fahrenheit = (celsius * 9/5) + 32

print(celsius, "градусов Цельсия равно", fahrenheit, "градусов Фаренгейта")


# input() - получает ввод пользователя как строку
# float() - преобразует строку в число с плавающей точкой
# итого: float(input())

# используется формула (n * 9/5) + 32 для конвертации

# print - вывод результата, с передачей аргументов задачи.

